# Build 3D Interpolators
interp_Wp  = RegularGridInterpolator((alpha_grid, Np_grid, PR_grid), Wp_data, bounds_error=False, fill_value=None)
interp_eff = RegularGridInterpolator((alpha_grid, Np_grid, PR_grid), eff_data, bounds_error=False, fill_value=None)

def HPT_PyCycle(T40, P40, P42, NH):
    """
    HPT component function scaling and querying the pyCycle 3D map data arrays.
    """
    # 1. Calculate Corrected Speed and Pressure Ratio
    theta = T40 / 288.15
    delta = P40 / 101325.0
    
    # Map speed parameter NpMap represents percentages (e.g. 100.0 = design)
    design_speed_rpm = 10000.0  
    Np_current = (NH / design_speed_rpm) / np.sqrt(theta) * 100.0
    PR_actual = P40 / max(1000.0, P42)
    
    # Protect array lookup boundaries against numerical transient overshoots
    Np_clamped = np.clip(Np_current, Np_grid.min(), Np_grid.max())
    PR_clamped = np.clip(PR_actual, PR_grid.min(), PR_grid.max())
    alpha_current = 1.0  # Holding variable nozzle area constant at map default
    
    # 2. Interrogate Interpolators
    eval_coords = np.array([alpha_current, Np_clamped, PR_clamped])
    Wp_lbs = float(interp_Wp(eval_coords)[0])
    eta_hpt = float(interp_eff(eval_coords)[0])
    
    # 3. De-correct Mass Flow back to true physical metrics (lbm/s -> kg/s)
    W42_lbs = Wp_lbs * delta / np.sqrt(theta)
    W42_kg_per_sec = W42_lbs * 0.453592
    
    # 4. Aerothermal Exit Temperature Drop (T42)
    gamma_gas = 1.33  # Hot turbine expansion specific heat ratio approximation
    T42_new = T40 - T40 * eta_hpt * (1.0 - (1.0 / PR_clamped) ** ((gamma_gas - 1.0) / gamma_gas))
    
    return W42_kg_per_sec, T42_new, eta_hpt

